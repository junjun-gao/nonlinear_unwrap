import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")

import matplotlib.pyplot as plt


# ============================================================
# 1. 输入输出路径
# ============================================================

PROPOSED_CSV_PATH = "/data/test/junjun/nonlinear_unwrap/simulation/nonlinear_unwrap/4_result_csv/simulated_unwrapped_noise_0p2rad_result.csv"
TRADITIONAL_CSV_PATH = "/data/test/junjun/nonlinear_unwrap/simulation/nonlinear_unwrap/4_result_csv/simulated_unwrapped_grid_noise_0p2rad_result.csv"

SAVE_PNG = "six_deformation_types_compare.png"
SAVE_SVG = "six_deformation_types_compare.svg"


# ============================================================
# 2. 参数设置
# ============================================================

POINT_IDS = [26, 687, 306, 1589, 3001, 2992]

TYPE_NAMES = [
    "Stable",
    "Seasonal",
    "Quadratic",
    "Step",
    "Power-law",
    "Piecewise linear"
]

N_TIMES = 60

TRUE_COLS = [
    "deformation_t{:02d}_mm".format(i)
    for i in range(1, N_TIMES + 1)
]

EST_COLS = [
    "deformation_est_t{:02d}_mm".format(i)
    for i in range(1, N_TIMES + 1)
]


# ============================================================
# 3. 读取 CSV
# ============================================================

df_proposed = pd.read_csv(PROPOSED_CSV_PATH)
df_traditional = pd.read_csv(TRADITIONAL_CSV_PATH)


# ============================================================
# 4. 检查字段
# ============================================================

def check_columns(df, cols, name):
    missing_cols = [col for col in cols if col not in df.columns]

    if len(missing_cols) > 0:
        raise ValueError(
            "{} 中缺少以下字段：\n{}".format(name, missing_cols)
        )


check_columns(
    df_proposed,
    ["point_id"] + TRUE_COLS + EST_COLS,
    "所提方法 CSV"
)

check_columns(
    df_traditional,
    ["point_id"] + EST_COLS,
    "传统方法 CSV"
)


# ============================================================
# 5. 按 point_id 提取一条时间序列
# ============================================================

def get_series_by_point_id(df, point_id, cols, name):
    row = df[df["point_id"] == point_id]

    if row.empty:
        raise ValueError(
            "{} 中没有找到 point_id = {}".format(name, point_id)
        )

    if len(row) > 1:
        print(
            "Warning: {} 中 point_id = {} 不唯一，默认使用第一行".format(
                name,
                point_id
            )
        )

    return row.iloc[0][cols].values.astype(float)


# ============================================================
# 6. 绘图
# ============================================================

t = np.arange(1, N_TIMES + 1)

fig, axes = plt.subplots(
    nrows=6,
    ncols=1,
    figsize=(8, 10),
    sharex=True
)

for i, point_id in enumerate(POINT_IDS):
    ax = axes[i]

    # 真实形变：从所提方法 CSV 中读取即可
    true_series = get_series_by_point_id(
        df_proposed,
        point_id,
        TRUE_COLS,
        "所提方法 CSV"
    )

    # 所提方法估计结果
    proposed_series = get_series_by_point_id(
        df_proposed,
        point_id,
        EST_COLS,
        "所提方法 CSV"
    )

    # 传统方法估计结果
    traditional_series = get_series_by_point_id(
        df_traditional,
        point_id,
        EST_COLS,
        "传统方法 CSV"
    )

    ax.plot(
        t,
        true_series,
        linewidth=1.8,
        label="True deformation"
    )

    ax.plot(
        t,
        proposed_series,
        linewidth=1.6,
        linestyle="--",
        label="Proposed method"
    )

    ax.plot(
        t,
        traditional_series,
        linewidth=1.6,
        linestyle=":",
        label="Traditional method"
    )

    ax.set_ylabel("Deformation / mm")

    ax.text(
        0.02,
        0.85,
        "{}  |  point_id = {}".format(TYPE_NAMES[i], point_id),
        transform=ax.transAxes,
        fontsize=10,
        fontweight="bold"
    )

    ax.grid(
        True,
        linestyle="--",
        linewidth=0.5,
        alpha=0.5
    )

    if i == 0:
        ax.legend(
            loc="upper right",
            fontsize=9,
            frameon=True
        )

axes[-1].set_xlabel("Time epoch")

plt.tight_layout()
plt.savefig(SAVE_PNG, dpi=300, bbox_inches="tight")
plt.savefig(SAVE_SVG, bbox_inches="tight")
plt.close()

print("Figure saved:")
print(SAVE_PNG)
print(SAVE_SVG)